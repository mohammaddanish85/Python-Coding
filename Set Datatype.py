# A Set is an unordered collection of elements.
# Order of elements is not maintained in sets.
# Set does not accept duplicate elements.

s={23, 35, 61, 88, 99}      # Set consists of 5 elements.
print(s)                    # Printing the elements of set.

ch=set("Hello")             # Storing the elementsof sets in a variable.
print(ch)                   # Printing the elements of set.
