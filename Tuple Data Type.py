# Tuple is similar to list.
# A Tuple can have different type of elements.
# Tuple have () parenthesis.
# Unlike list, tuple elements cannot be modified.


tpl= (10, 22.5, 'Danish', 'Sahil' )             # Elements of Tuple

print(tpl[0])                                   # Print 0th value of tuple.

print(tpl[1:3])                                 # Print 2nd & 3rd element of tuple.
